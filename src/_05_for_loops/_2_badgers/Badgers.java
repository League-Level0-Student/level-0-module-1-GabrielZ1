package _05_for_loops._2_badgers;

public class Badgers {
	public static void main(String[] args) {

		int bgers = 0;
		int totalBgers = 12;


		int mrooms = 0;
		int totalMrooms = 2;

		int timesExecuted = 0;
		int timesToBeExecuted = 2;

		
		
		for(timesExecuted = 1; timesExecuted<=timesToBeExecuted; timesExecuted+=1) {




			for(bgers=1; bgers<=totalBgers; bgers+=1) {
				System.out.println("Badger,");
			}


			for (mrooms=1; mrooms<=totalMrooms; mrooms+=1) {
				System.out.println("Mushroom,");


			}



		}
		System.out.println("A Snake!!!");

	}

}
